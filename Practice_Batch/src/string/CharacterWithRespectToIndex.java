package string;

public class CharacterWithRespectToIndex {

	public static void main(String[] args) {

		// to get character with respect to index
		// the position starts from 0
		
		
		String d = "automation";
		
		//method1
		System.out.println(d.charAt(9));
		
		//method2
		char s = d.charAt(4);
		System.out.println(s);
	}

}
