public class IfElseIf03 {

	public static void main(String[] args) {

		int t = 300;
		int m = 400;
		
		if(t==m) {
			System.out.println("Condition1 is true");
		}
		else if (m<t) {
			System.out.println("condition2 is true");
		}
		else {
			System.out.println("all conditions are false");
		}
	}

}
