package decision_making_statement;

public class SwitchStatement03 {

	public static void main(String[] args) {

		// if hm cases me break nii lgayenge to true condition ke bad ke sb condition print honge
		int f = 7;
		switch(f) {
		case 2:
			System.out.println("case-1");
		case 5:
			System.out.println("case-2");
		case 7:
			System.out.println("case-3");
		case 9:
			System.out.println("case-4");
		default:
			System.out.println("all cases are false");
		}
	}

}
